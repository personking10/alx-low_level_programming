#include "3-calc.h"
/**
 * op_add - add
 * @a: int
 * @b: int
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract
 * @a: int
 * @b: int
 * Return: subtract
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply
 * @a: int
 * @b: int
 * Return: multiplie
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divided
 * @a: int
 * @b: int
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulos
 * @a: int
 * @b: int
 * Return: modulos
 */
int op_mod(int a, int b)
{
	return (a % b);
}
